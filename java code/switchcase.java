import java.util.*;
public class switchcase{
    
    public static void main(String[] args) {
        int day=7;
        switch(day){
            case 6:
                System.out.println("HOLIDAY");
                break;
            case 7:
                System.out.println("HOLIDAY");
                break;
            default:
                System.out.println("WORKING DAY");
                break;

        }
    }
}